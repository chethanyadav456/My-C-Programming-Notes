// Single statement in C

#include <stdio.h>


/**
 * @brief This is the main function of the program.
 *
 * The main function is the entry point of the program.
 * It prints "Hello, world!" to the console.
 *
 * @return int 0 indicating successful execution of the program.
 */
int main(void) {
    // Print "Hello, world!" to the console
    printf("Hello, world!\n");

    // Return 0 to indicate successful execution of the program
    return 0;
}

// Output: Hello, world!

// Multiple statements in C

#include <stdio.h>

/**
 * @brief This is the main function of the program.
 *
 * The main function is the entry point of the program.
 * It prints "Hello, world!" to the console twice.
 *
 * @return int 0 indicating successful execution of the program.
 */
int main(void) {
    // Print "Hello, world!" to the console
    printf("Hello, world!\n");

    // Print "Hello, world!" to the console again
    printf("Hello, world!\n");

    // Return 0 to indicate successful execution of the program
    return 0;
}

// Output: Hello, world!
//         Hello, world!

